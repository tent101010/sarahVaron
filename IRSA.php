                    <h3>The International Rett Syndrome Association</h3>
                    <p>The International Rett Syndrome Association (IRSA) was founded by parents in early 1984 and became incorporated in January of 1985 as a non-profit organization. IRSA is a partnership of parents and professionals united in purpose to bring greater understanding of Rett syndrome.</p>
                    <p>Membership is represented by all 50 states and 65 foreign countries. Members include parents, relatives, physicians, therapists, researchers and friends, all interested in providing a better future for girls with Rett syndrome. Julia Roberts has taken up the cause of Rett Syndrome since the discovery of Rett Syndrome in a close friend's daughter.</p>
                    <p>A national board of directors governs the organization. Directors are elected by the general membership for four-year terms. The Board of Directors sets policy and manages the fiscal direction of the IRSA, and represents the interests of the general membership.</p>
                    <p>The national Board of Directors is complemented by a Professional Advisory Board comprised of internationally renowned physicians and scientific investigators who are experts in Rett syndrome.</p>
                    <p>A Subcommittee of five members of the Professional Advisory Board make up the Scientific Review Committee, whose charge is to evaluate and recommend seed grants for funding through IRSA's Permanent Research Fund.</p>
                    <p>The association's national office operations can be categorized as follows:</p>
                    <ul>
                         <li>Information and referral services</li>
                         <li>Collaboration with researchers</li>
                         <li>Membership services</li> 
                         <li>Development and publication of accurate and objective literature</li>
                         <li>Maintenance of a professional library of materials on Rett syndrome</li>
                         <li>Family network</li>                              
                         <li>Maintenance of patient database</li>                              
                         <li>Coordination of annual conferences and seminars on Rett syndrome</li> 
                         <li>Publication of a quarterly newsletter</li>                              
                         <li>Administration of research grants</li>
                    </ul>
                    <p>For more information, visit <a href="http://www.rettsyndrome.org">http://www.rettsyndrome.org</a></p>
